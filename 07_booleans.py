# Booleanos es True or False (primera en mayuscula)

is_single = False
print(is_single)
print(type(is_single))

is_single = True
print(is_single)

# Operador not lo que hace es invertir el valor

print(not True)
print(not False)

# si quiero invertir el ultimo valor generado
is_single = not is_single
print(is_single)

