# string
# string "una cadena de texto o caracteres"
my_name = "Nicolas"
my_name = 'Santiago'
my_name = "12"
print('my_name =>', my_name)
print(type(my_name))

# int
# "numeros enteros"
my_age = 12
print('my_age =>', my_age)
print(type(my_age))

# float
# float (numeros decimales)

pi = 3.14
print("pi =>", pi)
print(type(pi))

# boolean
# Verdadero (True) o Falso (False)
is_single = False
print('is_single =>', is_single)
print(type(is_single))

# inputs - NO ES TIPO DE DATO
# Variables, datos provenientes del usuario 
my_age = input("Cual es tu edad?")
print('my_age =>', my_age)
print(type(my_age))


