# Operadores aritmeticos

print(10+10)
print(10-3)
print(10*5)
print(10/2)
print(10/3)

# Residuo de la división
print(10 % 3)

# Numero entero
print(10 // 3)

# Exponencial
print(2 ** 3)

# Operaciones aritmeticas como se resuelven
# Resuelve de izquierda a derecha las operaciones
# P (Parentesis)
# E (Exponentes)
# M (Multiplicación)
# D (División)
# A (Adición)
# S (Sustracción)

print(2 ** 3 + 3 - 7 / 1 // 4)

print(2 ** 3)
print((7 / 1) // 4)



